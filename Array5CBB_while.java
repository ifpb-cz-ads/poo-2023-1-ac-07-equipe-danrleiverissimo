public class Array5CBB_while {
    public static void main(String[] args) {
        String[] dias = {"Segunda", "Terça", "Quarta", "Quinta", "Sexta", "Sábado", "Domingo"};
        int i = 0;
        while (i < dias.length) {
            System.out.println(dias[i]);
            i++;
        }
    }
}
